namespace TL.EmotionalAI
{
    /// <summary>
    /// Five relationship types based on Triangle of Love combinations
    /// </summary>
    public enum RelationshipType
    {
        Dislike = -1,      // Negative feelings, avoidance
        Stranger = 0,      // Neutral, unknown
        Acquaintance = 1,  // Basic familiarity
        Friend = 2,        // Positive friendship
        CloseFriend = 3,   // Deep friendship
        Crush = 4,         // Romantic attraction
        Partner = 5        // Committed romantic relationship


    }
}